// Operadores de comparacion
let valor1, valor2;
valor1 = 20;
valor2 = 10;

document.write(valor1 > valor2, "<br>"); // Mayor que
document.write(valor1 < valor2, "<br>"); // Menor que
document.write(valor1 == valor2, "<br>"); // Igualdad
document.write(valor1 != valor2, "<br>"); // Desigualdad
document.write(valor1 >= valor2, "<br>"); // Mayor o igual que
document.write(valor1 <= valor2, "<br>"); // Menor o igual que