// Redondear a siguiente enteror en decimal .5
var precio = Math.round(399.53);
document.write("Precio redondeado ", precio, "<br>");

// Funcion techo 
var precio = Math.ceil(299.9);
document.write("Precio redondeado hacia arriba ", precio, "<br>");

// Funcion piso
var precio = Math.floor(299.9);
document.write("Precio redondeado hacia abajo ", precio, "<br>");

// Funcion seno
var seno = Math.sin(45); // Funcion de seno 
document.write("Seno de 45 grados: ", seno, "<br>");

// Exponente de Euler
var expo = Math.exp(2); // Exponente de Euler
document.write("Exponente de Euler elevado a 2: ", expo, "<br>");

// Logaritmo natural
var logaritmo = Math.log(10); // Logaritmo natural
document.write("Logaritmo natural de 10: ", logaritmo, "<br>");

// Valor absoluto
var valorAbsoluto = Math.abs(-10); // Valor absoluto
document.write("Valor absoluto de -10: ", valorAbsoluto, "<br>");

// Calcular valor maximo de secuencia
var maximo = Math.max(10, 20, 30, 40); // Valor maximo
document.write("Valor maximo entre 10, 20, 30, 40: ", maximo, "<br>");

// Calcular valor minimo de secuencia
var minimo = Math.min(10, 20, 30, 40); // Valor minimo
document.write("Valor minimo entre 10, 20, 30, 40: ", minimo, "<br>");

// Valor aleatorio
var aleatorio = Math.random(); // Valor aleatorio entre 0 y 1
document.write("Valor aleatorio entre 0 y 1: ", aleatorio, "<br>");

// Raiz cuadrada
var raizCuadrada = Math.sqrt(16); // Raiz cuadrada
document.write("Raiz cuadrada de 16: ", raizCuadrada, "<br>");

// Exponente
var exponente = Math.pow(4, 3); // 4 elevado a la 3
document.write("4 elevado a la 3: ", exponente, "<br>");