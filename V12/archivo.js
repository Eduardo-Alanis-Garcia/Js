// Operadores de asignacion, incremento y decremento

let a = 1;

document.write("Valor inicial de a: ", a, "<br>");
a ++; // Aunmenta valor de a en 1
document.write("Valor de a despues de a++: ", a, "<br>");
a += 5; // Aumenta valor de a en 5
document.write("Valor de a despues de a += 5: ", a, "<br>");
a --; // Disminuye valor de a en 1
document.write("Valor de a despues de a--: ", a, "<br>");
a-= 2; // Disminuye valor de a en 2
document.write("Valor de a despues de a -= 2: ", a, "<br>");
a*=2; // Multiplica valor de a por 2
document.write("Valor de a despues de a *= 2: ", a, "<br>");
a/=2; // Divide valor de a entre 2
document.write("Valor de a despues de a /= 2: ", a, "<br>");
a**=2; // Eleva valor de a al cuadrado
document.write("Valor de a despues de a **= 2: ", a, "<br>");